package cond;

public class CondEx4 {
    public static void main(String[] args) {
        double starPoint = 9.3;

        if (starPoint >9){
            System.out.println("'어바웃 타임'을 추천합니다.");
        }
        if (starPoint>8){
            System.out.println("'토이 스토리'를 추천합니다.");
        }
        if (starPoint>7) {
            System.out.println("'고질라'를 추천합니다.");
        }
    }
}
