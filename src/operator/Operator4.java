package operator;

public class Operator4 {
    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3; // (2*2) + (3*3)
        int sum4 = (2*2) + (3*3);
        System.out.println("sum3 = "+ sum3);
        System.out.println("sum4 = "+ sum4); // 조금이라도 복잡한 경우 웬만하면 괄호를 넣자
    }
}
